#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	unsigned char	*p;

	if (nmemb == 0 || size == 0)
		return (malloc(0));
	p = malloc(nmemb * size);
	if (!p)
		return (NULL);
	ft_memset(p, 0, (nmemb * size));
	return (p);
}
