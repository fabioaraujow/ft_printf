#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	char	*str_result;
	size_t	i;

	i = 0;
	str_result = calloc((ft_strlen(s) + 1), sizeof(char));
	if (!str_result)
		return (NULL);
	while (s[i])
	{
		str_result[i] = f(i, s[i]);
		i++;
	}
	return (str_result);
}
