#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*p;
	unsigned char	chr;
	size_t			i;

	p = (unsigned char *)s;
	chr = (unsigned char)c;
	i = 0;
	while (i < n)
	{
		if (*p == chr)
			return (p);
		p++;
		i++;
	}
	return (NULL);
}
