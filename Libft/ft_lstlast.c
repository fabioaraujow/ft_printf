#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*current;

	if (!lst)
		return (NULL);
	current = lst;
	while (current)
	{
		if (current->next == NULL)
			return (current);
		current = current->next;
	}
	return (NULL);
}
