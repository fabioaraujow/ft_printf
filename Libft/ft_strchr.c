#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	chr;
	char	*p;

	chr = (char) c;
	p = (char *) s;
	if (chr == '\0')
	{
		while (*p)
			p++;
		return (p);
	}
	while (*p)
	{
		if (*p == chr)
			return (p);
		p++;
	}
	return (NULL);
}
